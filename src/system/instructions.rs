use super::cpu::*;

#[rustfmt::skip]
#[derive(PartialEq)]
pub enum LoadByteTarget {
    A, B, C, D, E, H, L, HLI, HLD, HL, BC, DE, A16,
}

#[rustfmt::skip]
#[derive(PartialEq)]
pub enum LoadByteSource {
    A, B, C, D, E, H, L, D8, HL, HLI, HLD, A16, BC, DE,
}

#[rustfmt::skip]
#[derive(PartialEq)]
pub enum LoadWordSource {
    D16, HL, SP, SPr8,
}

#[rustfmt::skip]
pub enum LoadWordTarget {
    BC, DE, HL, SP, A16,
}

#[derive(PartialEq)]
pub enum LoadOtherTarget {
    A,
    A8,
    CAddress,
}

#[derive(PartialEq)]
pub enum LoadOtherSource {
    A,
    A8,
    CAddress,
}

pub enum StackTarget {
    BC,
    DE,
    HL,
    AF,
}

pub enum LoadType {
    Byte(LoadByteTarget, LoadByteSource),
    Word(LoadWordTarget, LoadWordSource),
    Other(LoadOtherTarget, LoadOtherSource),
}

pub enum Instructions {
    NOP(),
    HALT(),
    LD(LoadType),
    LDH(LoadType),
    INC(IncDecTarget),
    DEC(IncDecTarget),
    ADD(ArithmeticTarget, ArithmeticSource),
    ADD16(Arithmetic16Target),
    SUB(ArithmeticSource),
    JR(JumpTest),
    JP(JumpTest),
    ADC(ArithmeticSource),
    SBC(ArithmeticSource),
    AND(ArithmeticSource),
    XOR(ArithmeticSource),
    OR(ArithmeticSource),
    CP(ArithmeticSource),
    RST(RSTTargets),
    CPL(),
    JPHL(),
    CCF(),
    DI(),
    EI(),
    DAA(),
    SCF(),
    PUSH(StackTarget),
    POP(StackTarget),
    CALL(JumpTest),
    RET(JumpTest),
    RETI(),
    RLCA(),
    RRCA(),
    RRC(ArithmeticSource),
    RLA(),
    RRA(),

    /* CB Instructions */
    RLC(ArithmeticSource),
    RL(ArithmeticSource),
    RR(ArithmeticSource),
    SLA(ArithmeticSource),
    SRA(ArithmeticSource),
    SWAP(ArithmeticSource),
    SRL(ArithmeticSource),
    BIT(u8, ArithmeticSource),
    RES(u8, ArithmeticSource),
    SET(u8, ArithmeticSource),
}

